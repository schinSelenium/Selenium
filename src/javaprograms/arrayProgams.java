package javaprograms;

public class arrayProgams {

	public static void main(String[] args) {
		
		int[][] arrayi =  {{1,2,3},{4,5,6}};
		int i, j;
		
		for (i=0; i<=2; i++)
		{
			for (j=0; j<=2; j++)
			{
				System.out.print(arrayi[i][j]+" ");
			}
			System.out.println("");
		}
		
	}
}
